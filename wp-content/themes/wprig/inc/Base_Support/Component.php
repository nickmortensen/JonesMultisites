	/**
	 * Create options boxes from all the blend modes.
	 *
	 * @param string $identifier Will be the id attribute for the select and the for attribute for label.
	 * @param string $label Will be text for the label.
	 * @param array  $options An array of blendmodes.
	 * @param array  $default Which of the keys within the options array is selected.
	 */
	public function select_html( string $identifier, string $label = 'choose option', array $options = [], $default = '' ) : string {
		if ( empty ( $options ) ) {
			$options = $this->get_blend_modes();
		}

		$output  = "<label for=\"$identifier\" hidden>$label</label>";
		$output .= "<select name=\"$identifier\" id=\"$identifier\">";
		$output .= '<option value="">' . $label . '</option>';
		foreach ( $options as $key => $value ) {
			$output .= '<option value="' . $value . '">' . ucwords( str_replace( '-', ' ', $key ) ) . '</option>';
		}
		$output .= "</select><!-- end select#$identifier-->";
		return $output;
	}

